namespace ILNumerics.F2NET.Formatting {
    public enum PlusSignStates {
        /// <summary>
        /// Optional plus sign (default).
        /// </summary>
        S,
        /// <summary>
        /// No plus sign-
        /// </summary>
        SS,
        /// <summary>
        /// Obligatory plus sign.
        /// </summary>
        SP
    }
}