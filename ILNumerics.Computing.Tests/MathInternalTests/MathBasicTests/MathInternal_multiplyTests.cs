        public void MathInternal_multiply_CM_CM() {

            Array<double> A = counter(1.0, 1.0, 4, 3);
            Array<double> B = counter(1.0, 1.0, 3, 4);
            Array<double> C = multiply(A, B);
            Array<double> Res = new double[,] {
